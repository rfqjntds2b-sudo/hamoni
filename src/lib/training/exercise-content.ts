/** Get instructional content for a single exercise. Throws if id is unknown. */
export function getExerciseContent(id: ExerciseId): ExerciseContent {
  const content = CONTENT_MAP.get(id);
  if (!content) {
    throw new Error(`Unknown exercise id: ${id}`);
  }
  return content;
}

/** Get locale-aware exercise content via i18n (falls back to hardcoded Korean) */
export function getLocalizedExerciseContent(id: ExerciseId, locale: Locale): ExerciseContent {
  // For Korean, use hardcoded content directly (source of truth)
  if (locale === DEFAULT_LOCALE) {
    return getExerciseContent(id);
  }

  const t = createT(locale, 'exerciseContent');
  const testKey = t(`${id}.description`);

  // If the exerciseContent namespace doesn't have this exercise, fall back to hardcoded
  if (testKey === `${id}.description`) {
    return getExerciseContent(id);
  }

  // Build content from i18n
  const sensations: string[] = [];
  for (let i = 0; i < 4; i++) {
    const s = t(`${id}.sensations.${i}`);
    if (s !== `${id}.sensations.${i}`) sensations.push(s);
  }

  const commonMistakes: { mistake: string; fix: string }[] = [];
  for (let i = 0; i < 5; i++) {
    const m = t(`${id}.mistakes.${i}.mistake`);
    if (m !== `${id}.mistakes.${i}.mistake`) {
      commonMistakes.push({ mistake: m, fix: t(`${id}.mistakes.${i}.fix`) });
    }
  }

  const levelTips: Record<number, string> = {};
  for (let i = 1; i <= 5; i++) {
    const tip = t(`${id}.levelTips.${i}`);
    if (tip !== `${id}.levelTips.${i}`) levelTips[i] = tip;
  }

  return {
    id,
    description: t(`${id}.description`),
    sensations,
    soundDescription: t(`${id}.soundDescription`),
    commonMistakes,
    levelTips,
  };
}
